package Controller;

import Dao.Impl.TypeSupportCenterDaolmpl;
import Model.TypeSupportCenter;
import java.io.IOException;
import jakarta.servlet.ServletException;
import jakarta.servlet.http.HttpServlet;
import jakarta.servlet.http.HttpServletRequest;
import jakarta.servlet.http.HttpServletResponse;
import java.sql.Date;
import java.time.LocalDate;

/**
 *
 * @author PiPi
 */
public class AdminTypeSupportCenterCreate extends HttpServlet {

    // <editor-fold defaultstate="collapsed" desc="HttpServlet methods. Click on the + sign on the left to edit the code.">
    /**
     * Handles the HTTP <code>GET</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        request.getRequestDispatcher("/admin/question-type/qnaTypeAdd.jsp").forward(request, response);
    }

    /**
     * Handles the HTTP <code>POST</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        TypeSupportCenterDaolmpl db = new TypeSupportCenterDaolmpl();
        String name = request.getParameter("name");
        boolean status = db.insert(new TypeSupportCenter(name, Date.valueOf(LocalDate.now()), Date.valueOf(LocalDate.now())));
        response.sendRedirect(request.getContextPath() + "/admin/qna-type?status=" + status);
    }

}
